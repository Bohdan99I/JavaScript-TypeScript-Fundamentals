/*
jQuery:

Які завдання виконує бібліотека jQuery:
jQuery спрощує роботу з:
1. Маніпуляціями DOM.
2. Обробкою подій.
3. Анімаціями.
4. AJAX-запитами.
5. Сумісністю між браузерами.
*/

/*
Що спрощує дана бібліотека:
jQuery спрощує:
1. Вибір елементів DOM.
2. Зміни стилів і атрибутів.
3. Додавання та видалення елементів.
4. Обробку подій.
5. Створення анімацій та ефектів.
6. Виконання асинхронних запитів (AJAX).
*/

/*
Які методи фільтрації є в jQuery, коротко опишіть їх роботу:
1. .filter(selector): повертає елементи, які відповідають вказаному селектору.
2. .not(selector): повертає елементи, які не відповідають вказаному селектору.
3. .has(selector): повертає елементи, які містять нащадків, що відповідають вказаному селектору.
4. .is(selector): перевіряє, чи відповідає хоча б один елемент вказаному селектору.
*/

/*
Які методи роботи з подіями є в jQuery, коротко опишіть їх роботу:
1. .on(event, handler): додає обробник події.
2. .off(event, handler): видаляє обробник події.
3. .trigger(event): ініціює подію.
4. .click(handler): додає обробник події кліку.
5. .hover(handlerIn, handlerOut): додає обробники подій наведення та залишення курсору миші.
*/

/*
Які методи маніпуляції зі сторінкою є в jQuery, коротко опишіть їх роботу:
1. .html(content): отримує або встановлює HTML вміст елемента.
2. .text(content): отримує або встановлює текстовий вміст елемента.
3. .append(content): додає вміст в кінець елемента.
4. .prepend(content): додає вміст на початок елемента.
5. .after(content): додає вміст після елемента.
6. .before(content): додає вміст перед елементом.
7. .remove(): видаляє елемент з DOM.
8. .empty(): видаляє вміст елемента.
9. .css(property, value): отримує або встановлює значення CSS властивостей.
10. .attr(attribute, value): отримує або встановлює значення атрибутів.
*/
